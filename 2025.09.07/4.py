print(f"Выборка 1/4: {mean_4:.2f}°C (размер: {len(temperatures2023_4)})отклонение от генеральной:{mean_4 - mean_population:+.2f}°C")
print(f"Выборка 1/16: {mean_16:.2f}°C (размер: {len(temperatures2023_16)}) отклонение от генеральной: {mean_16 - mean_population:+.2f}°C")



