from data import temperatures_2023
import numpy as np

# Генеральная совокупность
population = temperatures_2023

# вычисляю среднее значение генеральной совокупности
mean_population = np.mean(population)
print(f"среднее значение генеральной совокупности: {mean_population:.2f}°C")
print(f"размер генеральной совокупности: {len(population)} дней")
print()

# создаю выборки с разным шагом и вычисляем средние
temperatures2023_2 = population[::2]  # каждое второе значение
temperatures2023_4 = population[::4]  # каждое четвертое значение
temperatures2023_16 = population[::16]  # каждое шестнадцатое значение

# вычисляю средние значения для каждой выборки
mean_2 = np.mean(temperatures2023_2)
mean_4 = np.mean(temperatures2023_4)
mean_16 = np.mean(temperatures2023_16)

# Выводим результаты
print("Результаты выборок:")
print(f"temperatures2023_2 (каждое 2-е значение):")
print(f"  размер выборки: {len(temperatures2023_2)} дней")
print(f"  среднее значение: {mean_2:.2f}°C")
print(f"  отклонение от генеральной: {mean_2 - mean_population:+.2f}°C")
print()

print(f"temperatures2023_4 (каждое 4-е значение):")
print(f"  размер выборки: {len(temperatures2023_4)} дней")
print(f"  среднее значение: {mean_4:.2f}°C")
print(f"  отклонение от генеральной: {mean_4 - mean_population:+.2f}°C")
print()

print(f"temperatures2023_16 (каждое 16-е значение):")
print(f"  размер выборки: {len(temperatures2023_16)} дней")
print(f"  среднее значение: {mean_16:.2f}°C")
print(f"  отклонение от генеральной: {mean_16 - mean_population:+.2f}°C")
print()

# сводная
